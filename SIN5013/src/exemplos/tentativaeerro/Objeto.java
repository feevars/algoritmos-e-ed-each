package exemplos.tentativaeerro;

/**
 * @author Fernanda Reis
 *
 */
public class Objeto {

	private Integer valor;
	private Double peso;
	
	public Objeto(Integer valor, Double peso) {
		super();
		this.valor = valor;
		this.peso = peso;
	}
	public Integer getValor() {
		return valor;
	}
	public Double getPeso() {
		return peso;
	}
	
	
}
